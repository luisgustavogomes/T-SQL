,(8     ,1.0007967197)
,(9     ,1.0007967197)
,(10    ,1.0007967197)
,(11    ,1.0000000000)


;WITH ACUMULADO AS (
SELECT SEQ, VLR, VLR AS VLR_ACUMULADO 
  FROM #T
  WHERE SEQ = 1

UNION ALL
SELECT T.SEQ, T.VLR, CAST ((A.VLR_ACUMULADO * T.VLR) AS DECIMAL(15,10))
  FROM ACUMULADO AS A
 INNER JOIN #T AS T ON T.SEQ = (A.SEQ +1)
)
INSERT INTO #TT (SEQ, VLR, VLR_ACUMULADO)
SELECT SEQ, VLR, VLR_ACUMULADO
FROM ACUMULADO;

SELECT * FROM #TT